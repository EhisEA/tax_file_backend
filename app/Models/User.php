 * @method static \Illuminate\Database\Eloquent\Builder<static>|User whereStripeId($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User whereTrialEndsAt($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User whereUpdatedAt($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User withoutPermission($permissions)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User withoutRole($roles, $guard = null)
 * @mixin \Eloquent
 */
class User extends Authenticatable
{
    /** @use HasFactory<UserFactory> */
    use HasApiTokens, HasFactory, HasRoles, Notifiable, Billable;

    protected $fillable = ["phone_number", "email", "password"];

    protected $hidden = ["password", "remember_token"];

    protected $with = ["profile"];

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            "email_verified_at" => "datetime",
            "password" => "hashed",
        ];
    }

    /**
     * @return MorphTo<UserProfile|AccountantProfile,User>
     */
    public function profile(): MorphTo
    {
        return $this->morphTo();
    }

    /**
     * @return HasMany<TaxFiling,User>
     */
    public function tax_filings(): HasMany
    {
        return $this->hasMany(TaxFiling::class);
    }

    public function payments(): HasMany
    {
        return $this->hasMany(Payment::class);
    }
}
